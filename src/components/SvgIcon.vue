<script setup>
import { computed } from "vue";

const props = defineProps({
  name: {
    type: String,
    required: true,
  },
  size: {
    type: String,
    default: "16",
  },
  color: {
    type: String,
    default: "",
  },
});

const iconName = computed(() => `#icon-${props.name}`);
const svgClass = computed(() => {
  // console.log(props.name, "props.name");
  if (props.name) {
    return `svg-icon icon-${props.name}`;
  }
  return "svg-icon";
});
</script>

<template>
  <svg
    :class="svgClass"
    v-bind="$attrs"
    :style="{
      width: props.size + 'px',
      height: props.size + 'px',
      color: color,
    }"
  >
    <use :xlink:href="iconName" />
  </svg>
</template>

<style lang="scss">
.svg-icon {
  fill: currentColor;
}
</style>
